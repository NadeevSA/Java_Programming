public class MaxValues {
    private int MaxI;

    private int MaxJ;

    private int MaxVal;

    public MaxValues(int maxI, int maxJ, int maxVal) {
        MaxJ = maxJ;
        MaxI = maxI;
        MaxVal = maxVal;
    }

    public int getMaxI() {
        return MaxI;
    }

    public int getMaxJ() {
        return MaxJ;
    }

    public int getMaxVal() {
        return MaxVal;
    }
}
