public class PossibleMove {
    private int _coordX;
    private int _coordY;
    private int _score;

    public int getCoordX() {
        return _coordX;
    }

    public void setCoordX(int x) {
        _coordX = x;
    }

    public int getCoordY() {
        return _coordY;
    }

    public void setCoordY(int y) {
        _coordY = y;
    }

    public int getScore() {
        return _score;
    }

    public void setScore(int score) {
        _score = score;
    }
}
